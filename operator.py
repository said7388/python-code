num1 = int(input("Enter your first number :"))
num2 = int(input("Enter your second number :"))

# sumation result of both number

sum = num1 + num2
print("your summation result is", sum)

# substraction result

subsum = num1 - num2
print("your substraction result is", subsum)

# multiplication result of both number

mul = num1 * num2
print("your multiplication result is", mul)

# division result

div = num1 / num2
print("your division result is", div)

# floor result

floor = num1 // num2
print("your floor result is", floor)

# remainder result

rem = num1 % num2
print("your remainder result is", rem)